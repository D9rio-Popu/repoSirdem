    private void buscar(){
        Connection con = Conexion.Conexion.conexion();
        String cod = codigo.getText();
        try{
            ResultSet rs = Clases.Producto.Buscar(con, cod);
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);
            boolean encontrado =  false;
            while (rs.next()){
                Object[] fila = {
                    rs.getInt("id_producto"),
                    rs.getString("descripcion_producto"),
                    rs.getString("nombre_marca"),
                    rs.getString("estado")
                };
                modelo.addRow(fila);
                encontrado = true;
            }
            if (!encontrado && !codigo.getText().isEmpty()){
                agregar.setEnabled(true);
                modific = false;
            }
            rs.close();
            con.close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"Error al buscar el producto" +ex.getMessage());
        }   
    }
    
    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        int marc = marcamap.get(jComboBox2.getSelectedItem().toString());
        int estado = jRadioButtonActivo.isSelected() ? 1 : 0;
        if (modific){
            int codig = Integer.parseInt(jLcodigo.getText());
            try{
                Clases.Producto.Modificar(con, descripcion.getText(), marc, estado, codig);
            }catch(Exception ex){
                JOptionPane.showInternalMessageDialog(this,"Error no se pudo cargar el libro"+ ex.getMessage());
            }
        }else{
            try{
                Clases.Producto.Insertar(con, descripcion.getText(), marc, estado);
            }catch(Exception ex){
                JOptionPane.showInternalMessageDialog(this,"Error no se pudo cargar el libro"+ ex.getMessage());
            }
        }
        componentdesactivado();
        buscar();
        modific = false;
    }//GEN-LAST:event_guardarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int filaSeleccionada = jTable1.getSelectedRow();
        if (filaSeleccionada >= 0){
            jLcodigo.setText(jTable1.getValueAt(filaSeleccionada, 0).toString());
            descripcion.setText(jTable1.getValueAt(filaSeleccionada, 1).toString());
            jComboBox2.setSelectedItem(jTable1.getValueAt(filaSeleccionada, 2).toString());
            buttonGroup1.setSelected(
                "Activo".equals(jTable1.getValueAt(filaSeleccionada, 3).toString()) ? jRadioButtonActivo.getModel() : 
                "Inactivo".equals(jTable1.getValueAt(filaSeleccionada, 3).toString()) ? jRadioButtonInactivo.getModel() : null,
                true
            );
            modificar.setEnabled(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

